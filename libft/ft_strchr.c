char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*s_c;

	s_c = (char *) s;
	i = 0;
	while (s_c[i])
	{
		if (s_c[i] == c)
			return (&s_c[i]);
		i++;
	}
	if (c)
		return (0);
	return (&s_c[i]);
}
